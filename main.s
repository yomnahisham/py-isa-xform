        li t0, -1
        li t1, -1
        j update_coo

    #path 4 -> right up
    path4:
        li t0, 4
        bne s0, t0, path5
        li t0, 1
        li t1, 1
        j update_coo

    #path 5 -> left up
    path5:
        li t0, -1
        li t1, 1

    update_coo:

        lui s0, 0xF1        #y -> 0xF133
        slli s0, 1
        addi s0, 0x30
        
        lb s1, 0(s0)        #y = s1

        add s1, t1
        sb s1, 0(s0)
        mv t1, s0
        mv s0, s1       #save new y coordinates in s0

        addi t1, -1
        lb s1, 0(t1)
        add s1, t0
        sb s1, 0(t1)    #save new x coordinates in s1

    place_ball:
        li a0, 1        #put ball tile
        call updateBallTile
        li a0, 0    #no point made
        ret


