reset:
    #check conditions of game -> score
    lui t0, 0xF1
    slli t0, 1
    addi t0, 0x2C
    lb s0, 0(t0)    #s0 = scoreA

    li t1, 10
    bne s0, t1, co
    j endgame

co:
    addi t0, 1
    lb s0, 0(t0)    #s0 = scoreB
    bne s0, t1, co1
    j endgame

co1:
    #put ball tile
    li a0, 1        
    call updateBallTile 

    #put padels
    li a0, 2        
    call update_padelTile

    #check for w press
    li a0, 63
    addi a0, 56
start:
    ecall 7
    bz a1, start

match_loop:
    #call movepadel
    call moveball 
    bz a0, match_loop  
    j reset          #reset ball and padels and start new match 

endgame:
    ecall 10

updateBallTile:
    #load y and x into s0, s1   
        lui t0, 0xF1        #y -> 0xF131
        slli t0, 1
        addi t0, 0x31
        lb s1, 0(t0)

        addi t0, -1         #x -> 0xF130
        lb s0, 0(t0)

        lui t0, 0xF0
        slli t0, 1    #t0 = 0xF000

        slli s0, 2    # y*4
        add t0, s0    # t0 += y*4
        slli s0, 2    # y*16
        add t0, s0    # t0 += y*16
        add t0, s1    # t0 += x
        sb a0, 0(t0)
    ret

moveball:
        #remove ball tile
        li a0, 0        
        call updateBallTile
    
        lui t0, 0xF1
        slli t0, 1
        addi t0, 0x32
        lw s0, 0(t0)      #ball path

    #path 0 -> right
        bnz s0, path1
        li t0, 1
        li t1, 0
        j update_coo

    #path 1 -> left
    path1:
        li t0, 1
        bne s0, t0, path2
        li t0, -1
        li t1, 0
        j update_coo

    #path 2 -> right down
    path2:
        li t0, 2
        bne s0, t0, path3
        li t0, 1
        li t1, -1
        j update_coo

